								<p style="text-align:center; color:black; font-size:larger; font-weight:bold;"><img src="http://web.meteo.bmkg.go.id//media/data/bmkg/mfy/sur_idx.png" width="60%" height="60%" style="float:left; margin:0 9px 3px 0;" />Informasi <br></p>
								<div class="text-dark" style="text-align: justify; font-size:larger">
									<strong>A. Definisi</strong>
								</div>
								<p class="text-dark" style="text-align: justify; padding:10px">
									<strong>Cold Surge</strong> adalah jalaran udara dingin yang
									mengalir dari daratan Asia menuju wilayah
									Indonesia bagian barat. Hadirnya cold surge
									dapat ditandai dengan penguatan angin
									permukaan, penurunan suhu permukaan yang
									tajam, dan kenaikan tekanan permukaan di Laut
									Cina Selatan. Pada waktu yang bersamaan
									dengan hadirnya cold surge, dorongan massa udara
									dari belahan bumi selatan (BBS) bisa terjadi.
								</p>
								<div class="text-dark" style="text-align: justify; font-size:larger">
									<strong>B. Mekanisme</strong>
								</div>
								<p class="text-dark" style="text-align:justify; padding: 10px">
									Pada saat monsun dingin Asia berlangsung, seringkali terjadi penjalaran massa udara dingin dari pusat 
									tekanan tinggi di daratan Asia menuju ke arah Selatan dan ke arah Timur. 
									Penjalaran massa udara dingin ini kemudian dikenal dengan istilah Cold surge. 
									Meskipun masa aktifnya hanya dalam ordo hari namun cold surge 
									memiliki dampak cuaca yang merusak bagian Timur Asia dan juga berpengaruh 
									terhadap hujan diwilayah Asia Tenggara.
								</p>
