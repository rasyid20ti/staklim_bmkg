								<p class="text-dark" style="text-align:justify; padding:10px">
									<strong>Indeks monsun Asia (WNPMI)</strong> merupakan
									indeks monsun yang diperoleh dari rerata angin zonal
									pada level ketinggian 850 milibar dengan area cakupan
									100° BT – 130° BT, 5° LU – 15° LU dan 110° BT –
									140° BT, 20° LU – 30° LU.
									<br><br>
									Pada Waktu musim panas di BBU berbentuk 2 wilayah yang memiliki intensitas konvekti yang tinggi yakni dari teluk
									Benggala-India-laut Arabia dan laut Cina Selatan-Laut Filipina.
									<br><br>
									Pada bulan Sept-Maret pusat konvektif berada di barat Sumatera, pada bulan Mei pusat konvektif melintas ekuator dan
									masuk teluk Benggala bagian selatan sehingga pada bulan Mei-Juni hujan melonjat ke arah Utara di atas teluk Benggala dan bertahan hingga selesai musim panas.
									<br><br>
									Sedangkan untuk pasifik barat, pada bulan Des-Maret pusat konvergensi berada di sekita pulau Salmon,
									bulan Juni-Okt berada dilaut filipina, sedangkan bulan April, Mei, November berada pada 5N, 160E.

								</p>
