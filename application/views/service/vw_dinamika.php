								<p class="text-dark" style="text-align:justify; padding:10px">
								<strong>AUSMI</strong> merupakan singkatan dari Australia Monsun Index, didefinisikan sebagai
								nilai rata-rata angin zonal pada level ketinggian 850 milibar dalam area antara 5-15LS
								dan 110-130BT. Indeks monsun Australia tersebut merepresentasikan dengan sangat baik
								curah hujan monsunal di sebagian BMI dan di Australia bagian utara pada skala
								musiman, antarmusiman, tahunan, antartahunan, hingga skala 10-tahunan.<br><br>
								Fungsi dari indeks monsun Australia yaitu dapat dijadikan pedoman bagi
								variabilitas curah hujan di Australia bagian utara. Sebagaimana diketahui, curah hujan
								tahunan (annual oscillation) di Australia dipengaruhi kuat oleh intensifikasi daerah
								bertekanan rendah selama periode musim panas.

								</p>
